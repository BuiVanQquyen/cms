        /// <summary>
        /// Convert object to dictionary param
        /// </summary>
        /// <param name="obj"></param>
        /// <param name="bUpperKey"></param>
        /// <returns></returns>
        public static Dictionary<string, object> ConvertObjectToDictionary(object obj, bool bUpperKey = false)
        {
            Dictionary<string, object> dictionary = obj.GetType().GetProperties()
                                                    .ToDictionary(x => (bUpperKey ? x.Name.ToUpper() : x.Name), x => x.GetValue(obj));
            return dictionary;
        }

        public static string ConvertStreamToBase64(Stream stream)
        {
            byte[] bytes;
            using (var memoryStream = new MemoryStream())
            {
                stream.CopyTo(memoryStream);
                bytes = memoryStream.ToArray();
            }

            string base64 = Convert.ToBase64String(bytes);
            return base64;
        }

        /// <summary>
        /// remove vietnamese string
        /// </summary>
        /// <param name="str"></param>
        /// <returns></returns>
        public static string RemoveVietnameseSigns(string str)
        {
            if (!string.IsNullOrWhiteSpace(str))
            {
                string[] VietnameseSigns = { "aAeEoOuUiIdDyY", "áàạảãâấầậẩẫăắằặẳẵ", "ÁÀẠẢÃÂẤẦẬẨẪĂẮẰẶẲẴ", "éèẹẻẽêếềệểễ", "ÉÈẸẺẼÊẾỀỆỂỄ", "óòọỏõôốồộổỗơớờợởỡ", "ÓÒỌỎÕÔỐỒỘỔỖƠỚỜỢỞỠ", "úùụủũưứừựửữ", "ÚÙỤỦŨƯỨỪỰỬỮ", "íìịỉĩ", "ÍÌỊỈĨ", "đ", "Đ", "ýỳỵỷỹ", "ÝỲỴỶỸ" };
                for (int i = 1; i < VietnameseSigns.Length; i++)
                {
                    for (int j = 0; j < VietnameseSigns[i].Length; j++)
                        str = str.Replace(VietnameseSigns[i][j], VietnameseSigns[0][i - 1]);
                }
            }
            return str;
        }

